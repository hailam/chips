const std = @import("std");

// Font variants supported by chip-8-database's `fontStyle` enum. The
// small font is always 80 bytes (16 hex digits × 5 rows). The big font
// for SCHIP-style large-digit sprites is 160 bytes (10 rows per digit);
// most variants don't ship one, so we zero-fill those slots.
//
// Byte data sourced from John Earnest's Octo emulator.js
// (gh-pages branch) — the de-facto reference that the chip-8-database's
// `fontStyle` enum documents. `akouz1` isn't in Octo's table; we fall
// back to `octo` for it.
pub const FontStyle = enum {
    octo,
    vip,
    schip,
    dream6800,
    eti660,
    fish,
    akouz1,

    pub fn fromString(s: []const u8) ?FontStyle {
        if (std.ascii.eqlIgnoreCase(s, "octo")) return .octo;
        if (std.ascii.eqlIgnoreCase(s, "vip")) return .vip;
        if (std.ascii.eqlIgnoreCase(s, "schip")) return .schip;
        if (std.ascii.eqlIgnoreCase(s, "dream6800")) return .dream6800;
        if (std.ascii.eqlIgnoreCase(s, "eti660")) return .eti660;
        if (std.ascii.eqlIgnoreCase(s, "fish")) return .fish;
        if (std.ascii.eqlIgnoreCase(s, "akouz1")) return .akouz1;
        return null;
    }
};

pub const FontSet = struct {
    small: []const u8, // 80 bytes
    big: []const u8, // 160 bytes
};

