const big_empty = [_]u8{0} ** 160;

pub fn fontSet(style: FontStyle) FontSet {
    return switch (style) {
        .octo, .akouz1 => .{ .small = &small_octo, .big = &big_octo },
        .vip => .{ .small = &small_vip, .big = &big_empty },
        .schip => .{ .small = &small_octo, .big = &big_schip },
        .dream6800 => .{ .small = &small_dream6800, .big = &big_empty },
        .eti660 => .{ .small = &small_eti660, .big = &big_empty },
        .fish => .{ .small = &small_fish, .big = &big_fish },
    };
}

pub const default_style: FontStyle = .octo;

test "fontSet returns 80-byte small and 160-byte big arrays" {
    for (std.enums.values(FontStyle)) |style| {
        const fs = fontSet(style);
        try std.testing.expectEqual(@as(usize, 80), fs.small.len);
        try std.testing.expectEqual(@as(usize, 160), fs.big.len);
    }
}

test "FontStyle.fromString handles all variants case-insensitively" {
    try std.testing.expectEqual(FontStyle.octo, FontStyle.fromString("octo").?);
    try std.testing.expectEqual(FontStyle.octo, FontStyle.fromString("OCTO").?);
    try std.testing.expectEqual(FontStyle.vip, FontStyle.fromString("vip").?);
    try std.testing.expectEqual(FontStyle.schip, FontStyle.fromString("SCHIP").?);
    try std.testing.expectEqual(FontStyle.dream6800, FontStyle.fromString("dream6800").?);
    try std.testing.expectEqual(FontStyle.eti660, FontStyle.fromString("eti660").?);
    try std.testing.expectEqual(FontStyle.fish, FontStyle.fromString("fish").?);
    try std.testing.expectEqual(FontStyle.akouz1, FontStyle.fromString("akouz1").?);
    try std.testing.expect(FontStyle.fromString("nonsense") == null);
}

test "akouz1 falls back to octo glyph data" {
    try std.testing.expectEqualSlices(u8, fontSet(.octo).small, fontSet(.akouz1).small);
}